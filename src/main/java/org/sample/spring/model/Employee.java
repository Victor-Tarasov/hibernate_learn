package org.sample.spring.model;

public class Employee implements IEmployee {
    private Long id;
    private String name;

    public Employee() {
    }

    public Employee(String name) {
        this.name = name;
    }

    @Override
    public Long getId() {
        return id;
    }

    @Override
    public void setId(Long id) {
        this.id = id;
    }

    @Override
    public String getName() {
        return name;
    }

    @Override
    public void setName(String name) {
        this.name = name;
    }
}
